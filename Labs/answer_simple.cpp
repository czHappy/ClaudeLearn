/*
 * 数字梯形 - 简化版答案（更适合初学者）
 * 此版本更简洁，没有处理最后一个空格的逻辑
 */

#include <iostream>
using namespace std;

int main() {
    int n;
    cin >> n;

    // 外层循环：第 i 行
    for (int i = 1; i <= n; i++) {
        // 内层循环：输出 1 到 i
        for (int j = 1; j <= i; j++) {
            cout << j << " ";  // 直接输出数字和空格
        }
        cout << endl;  // 换行
    }

    return 0;
}

