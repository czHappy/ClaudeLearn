/*
 * 棋盘格子定位 - 标准答案
 * 文件：chess_position.cpp
 *
 * 问题：n×m棋盘按行从左到右填充1到n×m，给定k，求其位置
 */

#include <iostream>
using namespace std;

int main() {
    int n, m, k;
    cin >> n >> m >> k;

    // 计算行号：(k-1) 除以 m，再加 1
    // 为什么用 k-1？因为编号从1开始，要转换为从0开始的索引
    int row = (k - 1) / m + 1;

    // 计算列号：(k-1) 对 m 取余，再加 1
    int col = (k - 1) % m + 1;

    cout << row << " " << col << endl;

    return 0;
}

/*
 * 解题思路：
 *
 * 1. 观察规律：
 *    - 第1行：1, 2, 3, ..., m
 *    - 第2行：m+1, m+2, ..., 2m
 *    - 第r行：(r-1)*m+1, ..., r*m
 *
 * 2. 数学推导：
 *    假设k在第r行第c列，则：
 *    k = (r-1) * m + c
 *
 *    变形得：
 *    k-1 = (r-1) * m + (c-1)
 *
 *    所以：
 *    (r-1) = (k-1) / m  （整数除法，向下取整）
 *    (c-1) = (k-1) % m  （取余数）
 *
 *    因此：
 *    r = (k-1) / m + 1
 *    c = (k-1) % m + 1
 *
 * 3. 为什么要减1？
 *    - 如果k=8, m=4，直接用k/m = 2，会认为在第2行
 *    - 但实际上8=2*4，应该在第2行第4列（最后一列）
 *    - 用(k-1)/m = 7/4 = 1，加1后得到正确的第2行
 *    - 用(k-1)%m = 7%4 = 3，加1后得到正确的第4列
 *
 * 4. 验证示例：
 *    k=7, m=4:
 *    row = (7-1)/4 + 1 = 6/4 + 1 = 1 + 1 = 2 ✓
 *    col = (7-1)%4 + 1 = 6%4 + 1 = 2 + 1 = 3 ✓
 *
 *    k=1, m=5:
 *    row = (1-1)/5 + 1 = 0/5 + 1 = 0 + 1 = 1 ✓
 *    col = (1-1)%5 + 1 = 0%5 + 1 = 0 + 1 = 1 ✓
 *
 *    k=20, m=5:
 *    row = (20-1)/5 + 1 = 19/5 + 1 = 3 + 1 = 4 ✓
 *    col = (20-1)%5 + 1 = 19%5 + 1 = 4 + 1 = 5 ✓
 *
 * 时间复杂度：O(1)
 * 空间复杂度：O(1)
 */
